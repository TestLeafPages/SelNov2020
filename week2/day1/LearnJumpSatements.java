package week2.day1;

public class LearnJumpSatements {

	public static void main(String[] args) {
		
		
		for(int i = 1 ; i <= 10 ; i++) {
			
			if(i == 3) {
				System.out.println("Three");
				//continue;
				break;
			}
			
			System.out.println(i);
			
		}
		
		System.out.println("After for loop");

	}

}
