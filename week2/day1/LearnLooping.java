package week2.day1;

public class LearnLooping {

	public static void main(String[] args) {

		int x = 5;

		// until the condition is false the statements within while loop will be
		// executed
		/*
		 * while(x < 10) { System.out.println(x);
		 * 
		 * //x = x + 1; x++; }
		 */

		do {

			System.out.println(x);

		} while (x > 10);

		/*
		 * while(x > 10) { System.out.println(x); }
		 */

	}

}
